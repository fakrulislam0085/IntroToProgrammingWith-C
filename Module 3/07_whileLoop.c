#include <stdio.h>

// while loop:
// Repeats a block of code as long as the condition is true.
// Syntax:
// while (condition) {
//     // code to run while condition is true
// }

int main()
{
    int i;
    i =1;

    while(i<=8)
    {
        printf("%d\n",i);
        i=i+1;
    }
    
    return 0;
}
    